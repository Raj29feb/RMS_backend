import mongoose from "mongoose";

// Define a schema for users
const addressSchema = new mongoose.Schema({
  userId: {
    type: String,
    required: true,
  },
  addressLine1: {
    type: String,
    required: true,
  },
  addressLine2: {
    type: String,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  zipCode: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
});

// Create a model for the 'User' collection
export default mongoose.model("Address", addressSchema);
